package tiposdedatos;

public class Enteros {
	
	public static void main(String[] args) {
		
		// Formas de declarar variables tipo entero
		
		int numero = 10;
		
		int numero2;
		numero2 = 15;
		
		int numero3 = 2, numero5 = 3;
		
		System.out.println(numero2);
		
		// Tipo byte
		
		byte num1 = 20;
		
		
		// Variable constante de tipo entero
		final int variableConstante = 50;
		
		
		// Tipo Short
		short numeroShort = 122;
		
		// Tipo Long
		long numeroLargo = 1234567899;
		
		
		// Tipo double
		
		double numeroD = 12.9D;
		
		// Tipo flotante
		
		float numeroF = 10.20F;
		
		
		// Tipo String
		String palabra = "Hola Mundo";
		
		
		// Tipo char
		char caracter = 'a';
		
		
		// Tipo Boleanos
		boolean verdad = true;
		boolean falsedad = false;
		
		
	}

}
